    private void combCategoryItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_combCategoryItemStateChanged
        // TODO add your handling code here:
        String category = combCategory.getSelectedItem().toString();
        ArrayList<Product> list = ProductDao.getAllRecords();
        Iterator<Product> itr = list.iterator();
        DefaultTableModel dtm = (DefaultTableModel) tblProduct.getModel();
        dtm.setRowCount(0);
        while (itr.hasNext()) {
            Product product = itr.next();
            if (product.getCategory().equals(category)) {
                dtm.addRow(new Object[]{product.getName()});
//                txtProductName.setText(product.getName());
//                txtProductPrice.setText(product.getPrice());
            }
        }

    }//GEN-LAST:event_combCategoryItemStateChanged


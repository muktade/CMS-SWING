    private void tblUaerDetailsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblUaerDetailsMouseClicked
        // TODO add your handling code here:
        TableModel model = tblUaerDetails.getModel();
        int index = tblUaerDetails.getSelectedRow();
        String email = model.getValueAt(index, 2).toString();
        String status = model.getValueAt(index, 6).toString();
        if (status.equals("true")) {
            status = "false";
        } else {
            status = "true";
        }
        int a = JOptionPane.showConfirmDialog(this, "Do you want to change Status for "+email+"?","Yes",JOptionPane.YES_NO_OPTION);
        if(a==0){
            userDao.stasusUpdate(email, status);
            setVisible(false);
            new VerifyUser().setVisible(true);
        }
    }//GEN-LAST:event_tblUaerDetailsMouseClicked


    private void txtEmailFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtEmailFocusLost
        // TODO add your handling code here:
        String emailPattern = "^[a-zA-Z._0-9]{0,50}+[@]+[a-zA-Z]+[.]+[a-zA-z]+$";
        String validMobile = "^[0-9]{11,15}$";
        String validName = "^[a-zA-z]{0,30}$";
        String mobile = txtMobailNumber.getText();
        if (!txtCName.getText().equals("")) {
            if (!txtCName.getText().matches(validName)) {
                JOptionPane.showMessageDialog(this, "<html><b style=\"color:red\">Number is Not Allowed.</b></html>", "Message", JOptionPane.INFORMATION_MESSAGE);
                txtCName.requestFocus();
            } else if (!mobile.equals("")) {
                if (!mobile.matches(validMobile)) {
                    JOptionPane.showMessageDialog(this, "<html><b style=\"color:red\">Mobile Number Must Be Atlist 11 Digit Number.</b></html>", "Message", JOptionPane.INFORMATION_MESSAGE);
                    txtMobailNumber.requestFocus();
                } else if (!txtEmail.getText().equals("")) {
                    if (!txtEmail.getText().matches(emailPattern)) {
                        JOptionPane.showMessageDialog(this, "<html><b style=\"color:red\">Invalid Email!</b></html>", "Message", JOptionPane.INFORMATION_MESSAGE);
                        txtEmail.requestFocus();
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "<html><b style=\"color:red\">Email Can't Be Empty!</b></html>", "Message", JOptionPane.INFORMATION_MESSAGE);
                    txtEmail.requestFocus();
                }
            } else {
                JOptionPane.showMessageDialog(this, "<html><b style=\"color:red\">Mobile Number Can't Be Empty!</b></html>", "Message", JOptionPane.INFORMATION_MESSAGE);
                txtMobailNumber.requestFocus();
            }
        } else {
            JOptionPane.showMessageDialog(this, "<html><b style=\"color:red\">Name Is Needed.</b></html>", "Message", JOptionPane.INFORMATION_MESSAGE);
            txtCName.requestFocus();
        }

    }//GEN-LAST:event_txtEmailFocusLost


    private void combStatusItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_combStatusItemStateChanged
        // TODO add your handling code here:
        if (combStatus.getSelectedItem().equals("false")) {
            String email = "admin@gmail.com";
            String status = combStatus.getSelectedItem().toString();
            DefaultTableModel dtm = (DefaultTableModel) tblUaerDetails.getModel();
            dtm.setRowCount(0);///////////
            ArrayList<User> list = userDao.getAllUsers(email, status);
            Iterator<User> itr = list.iterator();
            while (itr.hasNext()) {
                User user = itr.next();
                if (!user.getEmail().equals("admin@gmail.com")) {
                    dtm.addRow(new Object[]{user.getId(), user.getName(), user.getEmail(), user.getMobileNumber(), user.getAddress(), user.getSecuryQuestion(), user.getStatus()});
                }
            }
        } else if (combStatus.getSelectedItem().equals("true")) {
            String email = "admin@gmail.com";
            String status = combStatus.getSelectedItem().toString();
            DefaultTableModel dtm = (DefaultTableModel) tblUaerDetails.getModel();
            dtm.setRowCount(0);
            ArrayList<User> list = userDao.getAllUsers(email, status);
            Iterator<User> itr = list.iterator();
            while (itr.hasNext()) {
                User user = itr.next();
                if (!user.getEmail().equals(email)) {
                    dtm.addRow(new Object[]{user.getId(), user.getName(), user.getEmail(), user.getMobileNumber(), user.getAddress(), user.getSecuryQuestion(), user.getStatus()});
                }
            }
        } else {
            DefaultTableModel dtm = (DefaultTableModel) tblUaerDetails.getModel();
            dtm.setRowCount(0);
            ArrayList<User> list = userDao.getAllUser("","");
            Iterator<User> itr = list.iterator();
            while (itr.hasNext()) {
                User user = itr.next();
                if (!user.getEmail().equals("admin@gmail.com")) {
                    dtm.addRow(new Object[]{user.getId(), user.getName(), user.getEmail(), user.getMobileNumber(), user.getAddress(), user.getSecuryQuestion(), user.getStatus()});
                }
            }
        }
    }//GEN-LAST:event_combStatusItemStateChanged

